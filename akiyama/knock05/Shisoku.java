package knock05;

public class Shisoku {
	public static void main(String[] args) {
		int a = 123;
		int b = 7;
		System.out.println(a + b);
		System.out.println(a - b);
		System.out.println(a * b);
		System.out.println(a / b);
		System.out.println(a % b);

		int c = 123;
		int d = 3;
		System.out.println(c + d);
		System.out.println(c - d);
		System.out.println(c * d);
		System.out.println(c / d);
		System.out.println(c % d);

	}
}
